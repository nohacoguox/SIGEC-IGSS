export const seedPuestos = async () => {
  const puestoRepository = AppDataSource.getRepository(Puesto);

  console.log('Iniciando seed de puestos...');

  try {
    for (const nombrePuesto of puestos) {
      const puestoExistente = await puestoRepository.findOne({ where: { nombre: nombrePuesto } });
      
      if (!puestoExistente) {
        const nuevoPuesto = puestoRepository.create({
          nombre: nombrePuesto,
          activo: true
        });
        await puestoRepository.save(nuevoPuesto);
      }
    }

    const totalPuestos = await puestoRepository.count();
    console.log(`Seed de puestos completado. Total de puestos en BD: ${totalPuestos}`);
  } catch (error) {
    console.error('Error durante el seed de puestos:', error);
    // No lanzamos el error para que la aplicación continúe
  }
};

// Si se ejecuta directamente este archivo
if (require.main === module) {
  AppDataSource.initialize()
    .then(async () => {
      console.log('Data Source inicializado');
      await seedPuestos();
      console.log('Seed completado exitosamente');
      process.exit(0);
    })
    .catch((error) => {
      console.error('Error durante el seed:', error);
      process.exit(1);
    });
}
